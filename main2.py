оценки = [5, 4, 3, 5, 2, 4, 5, 3, 4, 5, 2, 3, 5, 4, 2]
количество = {}

for оценка in оценки:
    количество[оценка] = количество.get(оценка, 0) + 1

сортировка = dict(sorted(количество.items()))
print("Количество каждой оценки:", сортировка)

средний = sum(оценки) / len(оценки)
print("Средний балл:", средний)

отсортированные = sorted(оценки)
n = len(отсортированные)
if n % 2 == 1:
    медиана = отсортированные[n // 2]
else:
    медиана = (отсортированные[n // 2 - 1] + отсортированные[n // 2]) / 2
print("Медиана:", медиана)

сдало = sum(1 for оценка in оценки if оценка >= 3)
print("Сдали экзамен:", сдало, "студентов")

отсортированные_по_частоте = sorted(количество.items(), key=lambda x: x[1])
оценки_по_частоте = [оценка for оценка, частота in отсортированные_по_частоте]
print("Оценки по частоте (возрастание):", оценки_по_частоте)
